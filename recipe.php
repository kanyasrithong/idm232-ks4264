        <h1>Step 1</h1>
        <p>
          Remove the honey from the refrigerator to bring to room temperature. Place
          an oven rack in the center of the oven; preheat to 450°F. Heat a medium pot
          of salted water to boiling on high. Line a sheet pan with foil. Remove and
          discard the netting from the turkey; season with salt and pepper on both
          sides. In a large pan, heat 2 teaspoons of olive oil on medium-high until hot.
          Add the seasoned turkey, skin side down. Cook 4 to 6 minutes on the first
          side, or until lightly browned. Flip and cook 3 to 5 minutes, or until lightly
          browned. Leaving any browned bits (or fond) in the pan on the stove, transfer
          to the sheet pan, skin side up. Roast 15 to 17 minutes, or until cooked
          through. (An instant-read thermometer should register 165ºF.) Transfer to a
          cutting board and let rest for at least 5 minutes.
        </p>
